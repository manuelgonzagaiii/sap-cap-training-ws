#!/usr/bin/env node
// Generate EduTools answer placeholders from marked-up solution templates.
//
// Authoring model ("Option A" answer placeholders):
//   * Write the FULL, correct, runnable solution in a template file named
//     <realname>.tmpl (e.g. db/schema.cds.tmpl, srv/maintenance-service.js.tmpl).
//   * Wrap each span the learner must type with a marker:
//         [[PH:hint shown in the blank]]the real answer text[[/PH]]
//   * This tool strips the markers to produce the clean, runnable file(s) and writes the
//     placeholder offset/length/placeholder_text into task-info.yaml.
//
// Why a script: offsets are positions into the solution file, so any edit shifts them.
// Re-running regenerates them exactly instead of counting by hand. Offsets are computed
// as UTF-8 BYTE positions into the clean file (the unit verified against the reference
// EduTools courses); clean files are written with LF endings. Keep blanks within ASCII
// spans where you can, and confirm any non-ASCII blank renders correctly in the IDE.
//
// Usage:
//   node tools/build-placeholders.mjs <task_dir> [<task_dir> ...]
//   node tools/build-placeholders.mjs --all      # every *.tmpl under the repo
//   node tools/build-placeholders.mjs <task_dir> --check   # verify only, write nothing
//
// Node, no dependencies. Port of the Python reference course's build_placeholders.py.

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const OPEN = "[[PH:";
const HINT_END = "]]";
const CLOSE = "[[/PH]]";

const args = process.argv.slice(2);
const CHECK = args.includes("--check");
const ALL = args.includes("--all");
const rel = (p) => path.relative(ROOT, p) || ".";
const bytes = (s) => Buffer.byteLength(s, "utf8");
const yamlStr = (s) => JSON.stringify(String(s)); // single-line double-quoted YAML scalar

function parseTemplate(text) {
  let clean = "";
  const placeholders = [];
  let i = 0;
  while (i < text.length) {
    const start = text.indexOf(OPEN, i);
    if (start === -1) {
      clean += text.slice(i);
      break;
    }
    clean += text.slice(i, start);
    const hintEnd = text.indexOf(HINT_END, start + OPEN.length);
    if (hintEnd === -1) throw new Error(`unterminated hint in marker near index ${start}`);
    const hint = text.slice(start + OPEN.length, hintEnd);
    const ansStart = hintEnd + HINT_END.length;
    const close = text.indexOf(CLOSE, ansStart);
    if (close === -1) throw new Error(`missing ${CLOSE} after marker near index ${start}`);
    const answer = text.slice(ansStart, close);
    placeholders.push({ offset: bytes(clean), length: bytes(answer), placeholder_text: hint });
    clean += answer;
    i = close + CLOSE.length;
  }
  return { clean, placeholders };
}

// Skip VCS/IDE/marker noise, but DO descend into legitimate config dotfiles such as
// .cdsrc.json / .env that can be part of a task.
const SKIP_WALK = new Set([
  ".git", ".idea", ".claude", ".junie", "node_modules", ".DS_Store",
  ".scaffold-generated", ".populated",
]);

function walk(dir) {
  const out = [];
  for (const name of fs.readdirSync(dir)) {
    if (SKIP_WALK.has(name)) continue;
    const p = path.join(dir, name);
    const st = fs.statSync(p);
    if (st.isDirectory()) out.push(...walk(p));
    else out.push(p);
  }
  return out;
}

const IGNORE_NAMES = new Set(["task.md"]);
const isInfoYaml = (n) => /(^|\/)(task|lesson|section|course)(-remote)?-info\.yaml$/.test(n);
const isTest = (relName) => relName === "test" || relName.startsWith("test/") || /\.test\.(js|ts)$/.test(relName);

function buildTaskInfo(entries) {
  // entries: { name, visible, placeholders?, propagatable? }
  const lines = ["# Auto-generated by tools/build-placeholders.mjs - edit the .tmpl, not this file.", "type: edu", "files:"];
  for (const e of entries) {
    lines.push(`  - name: ${e.name}`);
    lines.push(`    visible: ${e.visible}`);
    if (e.propagatable === false) lines.push("    propagatable: false");
    if (e.placeholders && e.placeholders.length) {
      lines.push("    placeholders:");
      for (const p of e.placeholders) {
        lines.push(`      - offset: ${p.offset}`);
        lines.push(`        length: ${p.length}`);
        lines.push(`        placeholder_text: ${yamlStr(p.placeholder_text)}`);
      }
    }
  }
  return lines.join("\n") + "\n";
}

function processTask(taskDir) {
  taskDir = path.resolve(taskDir);
  const all = walk(taskDir);
  const tmpls = all.filter((p) => p.endsWith(".tmpl")).sort();
  if (!tmpls.length) return null;

  console.log(`${rel(taskDir)}:`);
  const generated = new Set();
  const editable = [];

  for (const tmpl of tmpls) {
    const text = fs.readFileSync(tmpl, "utf8");
    const { clean, placeholders } = parseTemplate(text);
    // self-check: every placeholder slices back to a real byte span of the clean file
    const cleanBuf = Buffer.from(clean, "utf8");
    for (const p of placeholders) {
      const span = cleanBuf.slice(p.offset, p.offset + p.length);
      if (span.length !== p.length) throw new Error(`placeholder runs past end of file in ${rel(tmpl)}`);
    }
    const outPath = tmpl.slice(0, -".tmpl".length);
    const name = path.relative(taskDir, outPath).split(path.sep).join("/");
    generated.add(name);
    if (!CHECK) {
      fs.mkdirSync(path.dirname(outPath), { recursive: true });
      fs.writeFileSync(outPath, clean); // LF preserved as authored
    }
    editable.push({ name, visible: true, placeholders });
    console.log(`  ${name}  (${placeholders.length} placeholder${placeholders.length === 1 ? "" : "s"})`);
  }

  // Classify the remaining real files: tests are hidden; other non-generated, non-meta
  // files are shown to the learner as read-only context.
  const context = [];
  const tests = [];
  for (const p of all) {
    const name = path.relative(taskDir, p).split(path.sep).join("/");
    if (p.endsWith(".tmpl")) continue;
    if (generated.has(name)) continue;
    if (isInfoYaml(name) || IGNORE_NAMES.has(name)) continue;
    if (isTest(name)) tests.push({ name, visible: false, propagatable: false });
    else context.push({ name, visible: true });
  }

  const entries = [...editable, ...context, ...tests];
  if (!CHECK) fs.writeFileSync(path.join(taskDir, "task-info.yaml"), buildTaskInfo(entries));
  return entries;
}

function main() {
  let dirs = args.filter((a) => !a.startsWith("--")).map((d) => path.resolve(d));
  if (ALL) {
    for (const p of walk(ROOT)) {
      if (p.endsWith(".tmpl")) {
        const d = path.dirname(p);
        if (!dirs.includes(d)) dirs.push(d);
      }
    }
  }
  if (!dirs.length) {
    console.error("give one or more task directories, or --all");
    process.exit(2);
  }
  let total = 0;
  for (const d of dirs) {
    const result = processTask(d);
    if (result === null) console.log(`${rel(d)}: (no *.tmpl found - skipped)`);
    else total += 1;
  }
  console.log(`\n${CHECK ? "Checked" : "Generated placeholders for"} ${total} task(s).`);
}

main();
